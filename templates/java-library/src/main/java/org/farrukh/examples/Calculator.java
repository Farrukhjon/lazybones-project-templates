package org.farrukh.examples;

/**
 * Digit calculator.
 */
public class Calculator {

    /**
     * Adds to integer parameters and returns result.
     *
     * @param a - first integer parameter
     * @param b - second integer parameter
     * @return addition as a resutl.
     */
    public int add(final int a, final int b) {
        return a + b;
    }

}
